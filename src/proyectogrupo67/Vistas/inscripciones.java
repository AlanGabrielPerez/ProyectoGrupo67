    private void cargarAlumnos(){
        for (Alumno a: aluData.listarAlumnos()){
            jcbAlumnos.addItem(a);
        }
    
    }
    private void armarCabecera() {
        modelo.setColumnCount(0);
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Año");
        
        jtMaterias.setModel(modelo);
    }

